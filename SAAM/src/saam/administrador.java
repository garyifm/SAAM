    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals(doctor))
        {
            jTextField3.setText(doctor);
            jTextField2.setText(pass);
        }
        else if(jTextField1.getText().equals(temp))
        {
            jTextField3.setText(temp);
            jTextField2.setText(tempPass);
        }
        else 
        {
            jTextField4.setText("Usuario no encontrado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

